int	ft_atoi(char *str)
{
	int	i;
	int	x;
	int	y;

	i = 0;
	x = 1;
	y = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x *= -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		y = y * 10 + str[i] - 48;
		i++;
	}
	return (x * y);
}
