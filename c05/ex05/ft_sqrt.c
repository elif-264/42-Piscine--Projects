int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb == 1)
		return (1);
	else if (nb > 1)
	{
		while (a < nb && a < 46341)
		{
			if (a * a == nb)
				return (a);
			a ++;
		}
	}
	return (0);
}
