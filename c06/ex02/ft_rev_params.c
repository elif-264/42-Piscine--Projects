#include <unistd.h>

int	main(int ac, char **av)
{
	int	a;
	int	i;

	a = ac - 1;
	while (a > 0)
	{
		i = 0;
		while (av[a][i] != '\0')
		{	
			write(1, &av[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		a--;
	}
	return (0);
}
